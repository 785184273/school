$(function (){
	$("#info").validate({
		debug:true,
		rules:{
			user:{
				required:true,
				rangelength:[3,16]
			},
			password:{
				required:true,
				rangelength:[3,16]
			},
			passwordconfirm:{
				required:true,
				equalTo:"#password"
			},
			username:{
				required:true,
				rangelength:[1,5]
			},
			phone:{
				required:true,
				phone:true
			},
			email:{
				required:true,
				email:true
			}
		},
		messages:{
			user:{
				required:"请输入您的用户名",
				rangelength:"用户名的长度不符合要求请重新输入"
			},
			password:{
				required:"请输入您的密码",
				rangelength:"密码长度不符合要求请重新输入"
			},
			passwordconfirm:{
				required:"请再次输入密码",
				equalTo:"两次输入的密码不一致"
			},
			username:{
				required:"请输入您的姓名",
				rangelength:"请输入1-5个字符的姓名"
			},
			phone:{
				required:"请输入您的手机号码"
			},
			email:{
				required:"请输入您的邮箱"
			}
		},
		focusInvalid:true,
		errorClass:"text-danger",
		submitHandler:function(){
			console.log($("#info").serialize());
		}
	});
	$.validator.addMethod("email",function(value,element,params){
		var user_email =  /^([a-zA-Z0-9_-])+@([a-zA-Z0-9_-])+((.[a-zA-Z0-9_-]{2,3}){1,2})$/;
		return user_email.test(value);
	},"请输入正确的邮箱")

	$.validator.addMethod('phone',function(value,element,params){
		var user_phone = /^1[34578]\d{9}$/;
		return user_phone.test(value);
	},"请输入正确的手机号码")
})