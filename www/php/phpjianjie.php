<?php
	echo'i am a boy';
	/*
		php:超文本预处理器，是一种通用开源脚本语言
			php主要运行在服务器端
		php四种定界符：推荐使用标准风格
			标准风格：<?php
							php代码
						?>
			短标记风格：<?
							php代码
						?>
				默认是不支持段标记风格的，要在php的配置文件中（php.ini）中开启支持段标记
			asp标记风格：<%
						php代码
					%>
				默认是不支持asp风格的定界符，要在php.ini中开启
			script标记风格：<script language = "php">
								php代码
							</script>
		php支持省略结束标记，php开始定界符后面一直到最后全部都是php代码的情况下可以省略php结束符。		
			例如：<?php
						php语句
		1.php语句以分号结尾，
		2.php的关键字不区分大小写，变量名是区分大小写的
	*/
?>   
